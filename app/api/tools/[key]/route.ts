export async function POST(
  req: Request,
  { params }: { params: { key: string } }
) {
  const user = await getAuthUser();
  if (!user) return NextResponse.json({ error: "请先登录" }, { status: 401 });

  const key = params.key as ToolKey;
  const tool = TOOLS[key];
  if (!tool) return NextResponse.json({ error: "工具不存在" }, { status: 404 });

  const promptBuilder = PROMPTS[key];
  if (!promptBuilder) return NextResponse.json({ error: "工具暂未开放" }, { status: 404 });

  const credit = await checkCredits(user.id, tool.cost);
  if (!credit.ok) {
    return NextResponse.json({ error: "积分不足", code: "INSUFFICIENT_CREDITS" }, { status: 402 });
  }

  const input = await req.json();
  if (!input.topic?.trim()) {
    return NextResponse.json({ error: "请填写主题/内容" }, { status: 400 });
  }

  const prompt = promptBuilder(input);

  const openai = new OpenAI({
    apiKey: process.env.OPENAI_API_KEY,
    baseURL: process.env.OPENAI_BASE_URL || "https://api.deepseek.com",
  });

  try {
    const completion = await openai.chat.completions.create({
      model: process.env.OPENAI_MODEL || "deepseek-chat",
      messages: [
        { role: "system", content: "你是专业的内容创作专家，输出高质量、可直接使用的内容。" },
        { role: "user", content: prompt },
      ],
      temperature: 0.8,
    });

    const result = completion.choices[0]?.message?.content || "";

    await deductCredits(user.id, tool.cost);
    await logUsage(user.id, key, tool.cost);

    return NextResponse.json({ result, credits_used: tool.cost });
  } catch (error) {
    console.error(`Tool ${key} error:`, error);
    return NextResponse.json({ error: "生成失败，请重试" }, { status: 503 });
  }
}
